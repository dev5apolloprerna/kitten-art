                <h4>Overview</h4>
            <p>Hybrid-Cooled Shelters are high-efficiency cooling
units that integrate seamlessly with passive cooling
systems, providing reliable, maintenance-free
performance. The housing of the cooling unit is built
using corrosion-resistant materials to ensure long-
lasting
durability
and
protection
against
environmental factors like dust, sand, and humidity.
These units are typically mounted on the external
surface of the protective cabinet or shelter, ensuring
consistent
internal
temperatures
without
compromising the ingress protection (IP) rating of the
shelter.</p>
<h4>
Operating Principle
</h4>
<p>The cooling system operates using compressed
refrigeration technology, featuring the following core
components:</p>
<ul>
    <li> <i class="fa fa-check"></i>  Passive Cooling System</li>
    <li> <i class="fa fa-check"></i> Compressor</li>
    <li> <i class="fa fa-check"></i> Condenser</li>
    <li> <i class="fa fa-check"></i> Evaporator</li>
    <li> <i class="fa fa-check"></i> Expansion Valve</li>
</ul>
<p>The working principle of a hybrid-cooled shelter
combines active cooling with passive cooling
methods to enhance efficiency. The system uses a
refrigeration cycle with a compressor, condenser,
expansion valve, and evaporator to regulate the
internal temperature of the shelter. Passive cooling
techniques, such as thermal insulation and natural
ventilation, help reduce heat load by dissipating
excess heat through the shelter’s surface without
additional energy consumption.</p>
