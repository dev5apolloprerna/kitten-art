                <div class="sec-title">
                    <h2 class="">WORKING PRINCIPLE</h2>
                </div>
                <p>
                    FastCool™ system utilizes the diurnal temperature difference to provide cooling. It uses night cold
                    temperature to dissipate the heat gained during daytime via a heat pipe system and keeps the overall
                    temperature below specified limits.</p>
                <p>

                    FastCool™ system consists of a well-insulated tank/enclosure, thermal storage (water), internal heat
                    exchanger, and external heat exchanger. An internal heat exchanger located inside the water is
                    connected
                    to an external heat exchanger using a diode system. This connection only allows the heat to flow
                    from
                    the internal heat exchanger to the external heat exchanger and not the vice versa. As soon as the
                    ambient temperature drops below the tank water temperature, the cooling process starts
                    automatically.
                    The internal tank absorbs the heat from the water and expels it to the ambient via the external heat
                    exchanger.</p>
                <p>

                    When the ambient temperature rises above the water temperature during the daytime, the heat transfer
                    stops due to the diode nature of the system. Thermal insulation ensures that heat gain through the
                    tank
                    walls is negligible. Thus, the system can work in the highest recorded temperature environments.
                </p>
